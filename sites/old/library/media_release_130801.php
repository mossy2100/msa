<span class="bodytext">
The <a href="/jnt-db">Project Jarntimarra
Database (JNT-DB)</a> is now publicly available online. The database is a growing catalogue of 
Mars analogue sites in various locations throughout central Australia.  MSA believes that
Australia is one of the best places in the world for
undertaking field simulation testing of real Mars mission equipment and for
planning human missions using analogue equipment. The Australian outback resembles in
many ways the barren Martian surface.<P>

According to <a href="mailto:tech@marssociety.org.au">MSA Technical Director
Jason Hoogland</a>, this database is an important step in demonstrating the leadership
being shown by Australians in the area of Mars analogue research. "We
are starting from a low base, and need significantly more local professional
involvement, but we are now well on our way and the Jarntimarra-1 expedition
will add greatly to this database, which will be valuable for our technical
programme".<P>

MSA is planning a major expedition through South Australia and the Northern
Territory to scout for Mars-like locations to position a US$150,000 prototype
habitat/lander facility called Mars-Oz. Mars-Oz will look and feel like the
kind of spacecraft expected to carry the first humans to Mars.<P>

Jarntimarra means "star" in the Aboriginal Warlpiri language of central
Australia.<P>

<a href="mailto:president@marssociety.org.au">MSA President Guy Murphy</a>
points out that there is interest from organisations like NASA in this work.
"We see future users of the Jarntimarra database including the major space
agencies planning future human missions to Mars. It will also be useful to
Mars scientific researchers. For example, there has been work in Australia for
some time into evidence of fossilised extremophiles that is being pursued as
part of the search for evidence of life on the Red Planet."<P>

<a href="mailto:fundraising@marssociety.org.au">MSA Fundraising Director
Marshall Hughes</a> points out that the Society has over the past year put
in place the building blocks for an exciting technical programme. "All that
remains now is the funding," he says. "We offer very attractive exposure
opportunities for organisations looking to be associated with leading edge
research and Australian innovation". <P>
Media inquiries for interviews or further information about the database or
 forthcoming expedition should be directed to <a href="mailto:pr@marssociety.org.au">Jennifer Laing, MSA PR Director.

