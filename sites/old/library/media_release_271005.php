<br>

The UK Space Tourism Company, <a href="http://www.starchaser.co.uk/" target=_top>Starchaser Industries</a> through their Director,
Paul Young has generously donated  another UKP 20,000 (AU$ 46,723) for Stage
3 of the construction of the Starchaser <a href="http://www.marsupial.org.au">Marsupial</a> Rover project.
<br>
<br>

This funding will cover the final construction and fit out of the rover
before it becomes operational, which  is expect to be June 2006.
<br>
<br>

The analogue Starchaser Rover is being built by the Mars Society Australia
in Western Australia to replicate  the type of vehicle that would be used by
long distance explorers on the surface of the Red Planet. The  project arose
from a competition by the Mars Society in the US to raise awareness of the
need for vehicles to  provide simulations for crews prior to sending the
first interplanetary explorers to Mars.
<br>
<br><p>

Built to carry 2-3 persons, the Starchaser Rover is based on a stretched
long wheelbase forward control  Landrover chassis and running gear powered
by a Toyota engine. The vehicle will be convertible from full  cabin to ute
(pick-up) configuration, a d will be complete with airlock, toilet
facilities, head-up displays, GPS navigation equipment, spacesuits for
simulated EVAs, hydraulic robotic arm, long range fuel tanks etc.
<br>
<br>

The vehicle will be deployed to a planned Mars simulation base in the
Arkaroola region in SA for studies in a Mars-like environment, however its
home base will be at a purpose built rover laboratory at the <a href="http://www.hobbycentre.com.au/CPSSindex.html">Centre for
Planetary and Space Studies</a>, the latter a joint venture by the Mars Society
and Trinity College, East Perth, Western Australia.
