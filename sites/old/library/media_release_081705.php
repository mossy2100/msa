<span class="headlines">
ROVING THE RED PLANET VIA CANBERRA 
</span>
<br>
<br>

<span class="bodytext">
<b>Canberra - August 18, 2005 </b>
<br>
<br>

The 5th Australian Mars Exploration Conference will be held in Canberra this weekend, allowing locals and visitors to hear the latest news 
about Mars exploration, including findings from the surface of the Red Planet. The conference known as AMEC, is co-hosted this year by 
Mars Society Australia and the Australian National University's School of Psychology and gives national and international Mars 'experts'
 including scientists, engineers and specialists in human factors/psychology, a forum for networking and showcasing their latest research. 
Says Dr. Jonathan Clarke, conference convenor and Vice-President of Mars Society Australia, "This year's theme, 'Roving the Red Planet'
 reflects the prime importance of mobility in the exploration of Mars, both now and in the future and also recognises the fact that 2005 is 
Mars Society Australia’s 'Year of the Rover.' We are designing and constructing a simulated Mars Rover, the Starchaser Marsupial Rover, 
which we hope will contribute to the design database for future real Mars rover vehicles, and hope to be in a position to deploy it eventually
 at Arkaroola, the chosen site for the Australian Mars Research Base in the South Australian Outback. 
<br>
<br>

"The conference offers an exciting program of presentations covering diverse disciplines including geology, astrobiology, 
psychology/human factors, biomedicine, engineering and robotics. All the aspects in fact that will need to be considered by 
future mission planners. A highlight will be an address by Professor Steve Squyres on Sunday afternoon. 
<br>
<br>

"Steve Squyres is a principal investigator for NASA's Mars Exploration Rover (MER) Missions <i>Spirit </i>and <i>Opportunity </i>as 
well as Professor of Astronomy at Cornell University in Ithaca, New York. Along with his work on MER, he is also a co-investigator 
on the 2003 Mars Express and 2005 Mars Reconnaissance Orbiter missions, a member of the Gamma-Ray Spectrometer Flight
 Investigation Team for the Mars Odyssey mission, and a member of the imaging team for the Cassini to Saturn. Steve will be in 
Australia promoting his recently published book <i>Roving Mars. </I>
<br>
<br>

"Steve should be able to give us an insider's view of the latest Rover missions, as someone who has been privileged to be 
directly involved in the search for life on Mars." 
<br>
<br>

Neal Newman, NASA's representative to Australia, will provide an overview of future NASA missions to Mars during the 
conference and a large number of Australians will highlight their innovative research projects and contributions towards 
exploring the Red Planet. They include Dr. Christine Charles from the Australian National University covering new propulsion
 technology, Glen Nagle from Canberra's Tidbinbilla Tracking Station on tracking Mars rovers and Mars Society Australia's 
Natalie Cutler from Melbourne, giving an update on the MarsSkin spacesuit which featured recently on the Channel 7 
Beyond Tomorrow program and as a news item on the prestigious<i> Nature </i>magazine's Website. 
<br>
<br>

There will be a free public lecture on Friday 19 August at 8.00 pm at the Manning Clark Lecture Theatre, ANU, by Marion 
Anderson of Monash University. Marion will speak on the past, present and future of Mars exploration. Delegates will also 
enjoy an included field trip to Tidbinbilla and a 'behind the scenes' look at the Canberra Deep Space Communication Complex. 
This is one of three stations around the globe that form NASA's Deep Space Network, and the antenna dishes receive signals 
daily from craft such as those orbiting and exploring the surface of Mars. 
<br>
<br>

According to Jennifer Laing, PR Director of Mars Society Australia, AMEC demonstrates the depth of space-focused research 
that is taking place in Australia today as well as providing a window to the rest of the world's efforts via invited international speakers. 
"Each year, Mars Society Australia has put together an outstanding program of some of the world's leading Mars 'experts' and 
many of them are home-grown. We can feel very proud of the quality of the work that is being done in Australia, without the 
assistance of a national space agency and often lacking adequate funding for projects. Our people here have a real passion for 
space exploration and a preparedness to innovate and think outside the 'box' to get things done. And we are getting attention from
 the rest of the world for our work, which should send a powerful message to our politicians. This kind of research can have technology 
spinoffs and help create jobs and boost skills and knowledge in this country. It is also inspiring to our young people as it gives them a 
future to dream about." 
<br>
<br>

Those with an interest in space travel and Mars exploration in particular should get themselves to Canberra this weekend and take part 
in AMEC 2005. Registrations will be taken at the door and details of the conference program can be found on the Mars Society Australia 
or ANU Websites. 
