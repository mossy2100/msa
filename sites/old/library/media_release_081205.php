<span class="bodytext">
<b>December 1, 2005 </b>
<br>
<br>

Mars Society Australia's NSW Branch Coordinator, Michael West, was recently 
selected as a NSW finalist in the Young Australian of the Year Award for 2006.
While Michael wasn't chosen as the NSW recipient to go into the national finals, 
such an achievement was outstanding. We congratulate him for being selected for 
this prestigious award. Other state finalists in the Young Australian of the Year 
Awards include entertainer Guy Sebastian (SA) and world champion womens basketballer 
Lauren Jackson (ACT). Michael was presented with his finalists certificate by the 
Governor of NSW, Her Excellency Professor Marie Bashir AC, at a presentation ceremony 
at Government House in Sydney.
<br>
<br>

"As always, there was a high calibre of nominations in every state and territory 
which reminds us how many ordinary Australians there are out there doing extraordinary 
things," said Warren Pearson, National Director of the Nation al Australia Day Council.
<br>
<br>

At only 22, aerospace engineer Michael West is producing exciting world fir sts and 
putting Australia on the map in the eyes of the international aerospace community. His 
research as an engineering student at the University of Sydney has made a valuable 
contribution to the world's understanding of the effect s of the space environment 
upon polycarbonate materials. Michael has focussed in particular upon the damage caused 
by intense ultraviolet radiation and made several exciting discoveries. Michael's 
groundbreaking research could lead to the use of polycarbonate materials as shields 
against micrometeorites, wind ows aboard future spacecraft and as visors for the next 
generation of spacesuits for the Moon and Mars. Based on his work, polycarbonate materials 
also appear to be the material of choice for transparent greenhouses on the Moon and Mars 
which would be used for growing crops.
<br>
<br>

Michael has also designed, constructed and tested experiments that were launched aboard 
rockets from Woomera and was a participant on Mars Society Australias
2001 expedition around central Australia that located Mars-like terrain for a future 
research facility. At the age of 19, Michael organised the 2nd Australian Mars Exploration 
Conference, which showcased Australia's space exploration science and engineering activities, 
and assisted with the national speaking tour of Apollo 17 astronaut, Harrison Schmitt. 
Earlier this year Michael was appointed as a summer research scientist with the Search for 
Extraterrestrial Intelligence Institute in California and was invited to sp eak about the 
results of his research at the 43rd Aerospace Sciences Meeting in Nevada, an honour indeed 
for a student.
<br>
<br>

