<span class="headlines">
YOUNG AUSTRALIAN INVITED TO NASA FOR PLANETARY EXPLORATION PLANNING SESSION
</span>
<br>
<br>

<span class="bodytext">
<b>July 31, 2006 </b>
<br>
<br>

Australian Aerospace Engineer and manager of Mars Society Australia's Project
Marsskin, Dr. James Waldie, has been invited and funded by NASA to attend their
Next Generation Exploration Conference at the AMES Research Center next
month, as an international delegate.<br><br>
 
NASA has designed this event to bring together a very limited number of
emerging leaders, young professionals and students
with a keen interest in the long-term direction for human spaceflight.
It will allow them the opportunity to take part in a planning session &quot;to
develop strategies for human space exploration in the 21st century, as humanity
prepares to return to Earth's nearest neighbor in space: the Moon,&quot;
according to the conference Website ( http://ngec.arc.nasa.gov/). NASA has aimed
to include some foreign nationals at the session, as it recognises that
they are likely to play an active role in designing and implementing
future human space exploration missions.<br><br>
 
James, 29, who was awarded a PhD with respect to his research on new
technologies for spacesuits, is thrilled to be invited to take part in
this prestigious conference.  &quot;The Apollo program was before my time, so
a chance to be involved with NASA in future planetary exploration
planning is incredibly exciting.  I hope Australia can play a small but
important part in such pioneering endeavours of the future.&quot;  As Project
Leader of MarsSkin, he designs, produces and tests simulation space
suits that behave like real suits would on Mars.  These suits are worn
for spacewalks during full Mars habitation simulations performed by the
Mars Society in Australian and US deserts.  He has also attended the
International Space University's Summer School program in Adelaide in 2004,
and has received a great deal
of attention and media exposure for his research, including a segment on
TV's 'Beyond Tomorrow' and numerous radio and print interviews.

