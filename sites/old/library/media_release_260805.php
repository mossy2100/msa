MSA Welcomes Arkaroola Resort as New Corporate Member 
</span>
<br>
<br>

<span class="bodytext">
<b>August 26, 2005 </b>
<br>
<br>

The 
<A href="http://www.arkaroola.com.au" target=main>Arkaroola Wilderness Sanctuary and Resort</A>  has become a corporate member of Mars Society Australia. This is in recognition of their 
outstanding support for MSAs expeditions to the area, including 
Jarntimarra-1 and 
Expedition Two, and MSAs future research 
and education program. The Arkaroola Wilderness Sanctuary and Resort is situated in the northernmost Flinders Ranges in central South 
Australia, some 600km north of Adelaide and 130km east of the coal-mining town of Leigh Creek. The 610sq km wilderness sanctuary is 
operated by the Sprigg family and has advanced ecotourism accreditation for tourism featuring the geology, ecology, landscape and astronomical 
features of the area. <p>

MSA selected the Arkaroola region as its prime Australian Mars research region in 2001. The reasons included the fact 
that the Arkaroola region and its hinterland in a 200 km radius from the resort contains a diversity of features of interest 
to Mars geologists and astrobiologists. Furthermore, the diverse landscapes allow the testing of different equipment, and 
area that will contain the 
<A href="/marsoz.php" target=main>MARS-OZ station</A>
 can be isolated allowing the study of crew psycho-social interaction under 
field conditions. Also, thanks to the support of the Arkaroola Wilderness Sanctuary and Resort and the Sprigg family, the 
area is logistically accessible. Lastly the large numbers of tourists who visit this unique place provide a good opportunity for 
education and outreach about Mars exploration that is in keeping with the long standing role Arkaroola Wilderness Sanctuary 
and Resort in science and environmental education.<p>
We welcome our newest corporate members and look forward to developing our relationship with them as MSA continues to 
work towards the goal of human presence on the Red Planet.

