<br>

The MarsSkin team of James Waldie and Nat Cutler are building 15 simulated space suits for the 
<a href="http://www.vssec.org/index.html" target=_top> Victorian Space Science Education 
Centre</a> (VSSEC). The <a href="http://www.vssec.org/index.html" target=_top>VSSEC</a> was 
<a href="http://sci.esa.int/science-e/www/object/index.cfm?fobjectid=37894" target=_top>
 officially opened</a> in September this year by the Victorian premier.  
VSSEC is planning on taking its first students in in early 2006. 
<br>
<br>

The centrepiece of the <a href="http://www.vssec.org/index.html" target=_top>VSSEC</a>
 is a large simulated Martian environment in which students will carry out a range of scientfic and engineering tasks.  The space suits being provided by MSA will be worn by the students during these activities<p>
The space suits being provided are a development of the 
<a href="/marsskinexpeditiontwo.php" target=_top>
MarsSkin version 3.0</a> tested during 
<a href="http://chapters.marssociety.org/canada/expedition-mars.org/ExpeditionTwo/" target=_top>
Expedition 2</a> at Arkroola in 2004 and during the 
<a href="/marsskinlisaleonardo.php" target=_top>
Leonardo and Mona Lisa</a> expeditions to MDRS in 2005.  Termed MarsSkin 3.1, this version will 
simulate a conventional gas pressure suit.  In addition, compared to version 3.0, the new version 
will have a more rugged helmet and communications system and improved ventilation.  The backpack 
design will be changed in detail to improve appearance and to support the new ventilation system.
<br>
<br>

Money received from constructing the suits is being used to fund MarsSkin 4.0, which MSA hopes 
will be field trialed in 2006.    
MarsSkin 4.0 will feature the improved helmet and ventilation and modified backpacks of 
version 3.1 with a new rigid torso.<p>
