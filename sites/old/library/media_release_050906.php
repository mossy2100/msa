MSA Rocket Engine - First Test Firing Successful
</span>
<span class="bodytext">
<br>
<br>

<b>September 4, 2006 </b>
<br>
<br>
 
MSA is pleased to announce a major achievement by David Willson, MSA member and director, and also manager of the Mars oz project, in his hobby of scratch building rocket engines.  Along with several work colleagues, David  successfully test fired the rocket engine four times at a location 30 minutes drive from Hobart in Tasmania. Some 15 people came to the event and it was filmed as part of a documentary by MSA Western Australian member and film maker, Chris Dickinson. A paper on David's rocket engine will be a feature at <a href="/amec2006/amec2006.htm">AMEC 2006</a> in October at <a href="http://www.strathmore.vic.edu.au/VSSEC.php">VSSEC</a>. David's aim with this early prototype engine is to learn the art of designing and testing liquid propellant rocket engines. This experience may be used at a later stage to build and manage larger and more powerful rocket engine systems.<br><br>
  
The engine was fired horizontally, bolted to a frame that is in turn fixed to concrete. The engine is a kerosene, oxygen gas propellant system with pressurized nitrogen to feed the kerosene into the combustion chamber. The maximum operating pressure is 10 bar or 150 psi.<br><br>
  
The engine was controlled from a laptop using Visual Basic software located in a trench some 30 metres from the test firing structure. The control system, after the operator enables the RUN control, auto starts and stops the ignition and valve sequence that fires the engine. In addition the control system has a safety feature of monitoring the chamber pressure and thrust load sensor. During this test firing this feature was turned off.<br><br>
 
Other safety features are the use of pressurized nitrogen to purge out the combustion chamber after shutdown and water cooling in the combustion chamber walls made of brazed tubes.<br><br>
 
The intent of this test was to prove the operational safety, start up run and shut down sequence. This worked better than expected. The engine was run at low pressure at 2-3 bar. The photos show a yellow flame. The preferred flame at the nozzle exit is clear showing diamond shock fronts. Achieving this and a more efficient engine will be done at later tests.<br><br>

MSA wishes David every success in his endeavours.<br><br>

