<br>
<span class="bodytext">
			Mars is within reach.&nbsp; The fourth planet from the sun, the Red Planet, 
          is a potential new frontier for humanity. It is the nearest body in 
          the solar system known to possess all the mineral resources necessary 
          to sustain a permanent human presence.&nbsp; It has landscapes billions 
          of years old that can shed light on the early history of the earth and 
          the solar system. Understanding of its climatic history will help scientists 
          understand the operation of the earth's climate. Mars is the next most 
          likely place in the solar system where life may once or may still exist, 
          the discovery of which would offer deep insights into the origins of 
          life and the biological sciences. 
</span>
