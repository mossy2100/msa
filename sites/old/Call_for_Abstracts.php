Dear Colleagues,<P>

It is with pleasure that I invite you to submit abstracts for the 9th Australian Mars Exploration Conference, to be held in 
Adelaide on 17th - 19th July, 2009.<P>

This conference aims to expand the breadth and depth of scientific, technological and cultural development in 
support of the human exploration of Mars.

To this end, in addition to scientists and technologists, we extend our invitation to the social experts, managers, 
writers and artists who will be instrumental in forging such an enterprise.<P>

Since 2001, the AMEC conferences have been an international forum for Mars exploration. Past AMEC guests have included 
speakers such as Apollo 17 geologist Dr. Harrison Schmidt, space simulation expert Dr. Vadim Gushin of the Moscow 
Institute of Biomedical Problems and Dr. Steven Squires, Principle Investigator of NASA's Mars Exploration Rover team. <P>
 
AMEC conferences are run by the Mars Society Australia, an incorporated, non-profit research body that does practical 
research in support of an eventual human mission to Mars. MSA is the Australian branch of the worldwide Mars Society 
which has about 6,000 members. MSA funds and manages three major technical projects: the Starchaser Marsupial analogue 
rover, the MarsSkin analogue spacesuit and the Mars-Oz analogue research station, as well as a number of smaller 
experiments. Members publish widely in peer-reviewed international journals and papers, and conduct numerous public 
seminars and lectures.<P> 

The organisation has conducted two expeditions to central Australia. It has a high media 
profile, logging tens of impressions on radio, television, the Internet and the print media each year.<P> 

It is certified by the Australian Tax Office as a tax deductable gift certificate.  Its records are being archived by the National Library 
in Canberra as documents of historical importance.  MSA also participates in international collaborative projects with organisations such as NASA and ESA, including the Spaceward Bound educational initiative and a month-long simulation at the Mars Desert Research Station in Utah.<P>

