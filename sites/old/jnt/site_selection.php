<A NAME="jarntimarra"><h2>JARNTIMARRA 1</h2></a>
 
The Jarntimarra-1 expedition was undertaken to visit, evaluate and rank Australian Mars analogue sites for possible use by the MSA for future field reseach activities such as testing the
 <A HREF="http://www.marsupial.org.au/">Marsupial</A> analogue rover and possible future deployment of an Australian analogue research station.
<br>
<br>


Australia is rich in locations geologists have identified as closely analogous Mars to environments observed on the surface of Mars.  Initial electronic discussion between MSA members identified many of these, with further research identifying further sites of interest.  
Sites were nominated on the basis of scientific interest, the range of types of environments in the vicinity suitable for testing equipment, and visual resemblance to Mars. The most attractive sites were in central Australia, and most could be visited in a single 4WD trek.  
<br>
<br>

