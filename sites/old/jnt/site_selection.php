Based on the collective experience gained while filling in the site assessment sheets, each zone was rated on 5-point scale according to the above specific characteristics.  Engineering and science scores then were doubled to reflect their importance relative to the other criteria in the total score. On this basis, the Moon Plain, Woomera and Arkaroola zones achieved equal ranking.
<br>
<br>
 
To support the goal of recommending a premier site for the 2002-2003 season, the tie was broken by considering the arguments of individual expedition members advocating each zone. Eventually the case for the Arkaroola region prevailed by virtue of its unique combination of logistical convenience (hospitable base at nearby Arkaroola village; 8 hours road travel from Adelaide; 1200m all-weather airstrip at Balcanoona), international scientific reputation and Mars-like geology. 
<br>
<br>

Specific Mars analogue research in the region has been three-fold, focusing on aeolian landforms, extremophiles, and remote sensing.  Studies of aeolian landforms compared Martian dunes at Nili Petra with terrestrial dunes at Gurra Gurra Waterhole in the Strzelecki Desert. The extremophile work found radiation-resistant thermophiles in the Paralana hot spring which is characterised by high levels of radon gas.  The area has been used in remote sensing experiments comparing hyperspectral imagery from the alteration halo surrounding the Mount Painter fossil hydrothermal system with ground truth from a hand-held spectrometer.  This last study is particularly relevant to detecting the presence of such systems on Mars, which are believed to be good localities to search for microfossils. Potential Mars analogue geoscience research in the area may include palaeontology, geomorphology and regolith studies. The Proterozoic sediments of the area are known to host silicified microfossils and the sinters of the Mt. Gee fossil hydrothermal system show potential for microfossil preservation.  Geomorphological and regolith studies include evolution of the alluvial fans on the eastern flank of the Flinders Ranges, nature of mound springs of Lake Frome, and landscape evolution of the northern Flinders Ranges, where uplift has led to partial exhumation and dissection of ancient land surfaces buried beneath Cretaceous cover. 
<br>
<br>

<A NAME="conclusion"><h2>CONCLUSIONS</h2></a>
 
The Jarntimarra 1 expedition examined a wide range of sites across South Australia and the Northern Territory. Many sites were identified which had value as useful Mars analogues.
<br>
<br>

The preferred site for the proposed Australian analogue research station was on the gravel plains to the east of the Arkaroola zone's central point, between the eastern side of the northern Flinders Ranges and Lake Frome. This was chosen for a number of reasons. It will allow easy access to sites in the Flinders Ranges proper and on the plains that surround Lake Frome. There are many different terrains and areas of extremely high scientific interest in the vicinity. It will also simplify logistics, as a well-maintained, unsealed road runs up the eastern margin of the ranges, joining the Strzelecki Track to the north and the Barrier Highway to the south. The exact co-ordinates of the Hab site will be decided upon during a further expedition after discussion with relevant land holders.
