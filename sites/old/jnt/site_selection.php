<A NAME="selection"><h2>SELECTING THE REGIONS</h2></a> 

A number of key issues for MSA were resolved during a three-day conference at Arkaroola village at the end of Jarntimarra-1.  The expedition found that most of the assessed sites fell within the boundaries of only six 200-km diameter circles (Figure 2). The significance of these exploration zones is that each specifies a set of features within easy reach of one simulated Mars mission, given a vehicle capable of extended traverses.  If the centre of a circle represents a habitat landing site, all the features within that zone would be accessible by sorties of no more than 100km. Each of these six centres represents a possible "landing site" for MARS-OZ.   
<br>
<br>

