<p>The driest part of Australia is Lake Eyre (adjacent to the Sturt Stony
Desert) with an average rainfall of around 140 mm per annum and less than 30
rain days annually on average.</p>
<h2>Climate Averages</h2>
<p>Australian weather stations collect data at least daily, and BOM provides
averages for a range of parameters. The <a href="http://www.dnr.qld.gov.au">Queensland
Department of Natural Resources</a> provides interpolation of climate averages
for locations between stations (navigate to SILO website).</p>
