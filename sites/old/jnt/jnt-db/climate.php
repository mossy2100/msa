<h1>Weather Radar</h1>
<p>During Mars mission simulations, Mission Controllers can access an online
radar service provided by BOM at <a href="http://www.bom.gov.au">http://www.bom.gov.au</a>.
Regularly (10 minute) updated radar images obtained up to a radius of 130 km can
be viewed from a number of remote stations. These provide a near real-time
picture of local precipitation conditions. With a knowledge of current wind
conditions (prevailing from the west across the continent) this could provide
insight into imminent weather for field crews.</p>
