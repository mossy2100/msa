<h1>Climate Data</h1>
<p>This data has been obtained from the <a href="http://www.bom.gov.au">Australian
Bureau of Meteorology</a> (BOM). Further work is needed to acquire more
localised climate histories and predictions (3, 6 and 12 months, updated in this
document regularly). For example, monthly averages including solar radiation and
wind (for estimation of renewable power potential) and relative humidity
(relevant for analogue suit operations, for example) should be included. In
addition, typical daily weather profiles during various months would be useful
for operations planning.</p>
<h2>Climate Overview</h2>
