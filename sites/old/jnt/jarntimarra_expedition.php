<A NAME="introduction">
	 <h2>INTRODUCTION</h2> 
The Jarntimarra 1 Expedition spent two weeks in the Red Centre surveying Mars analogue sites for the <A href="/jnt/">Jarntimarra Database</a>. Three preferred regions were identified as possible locations for future Mars analogue research activities, with the Arkaroola region in the Northern Flinders Ranges ranked first along with Arkaringa near Coober Pedy and the Woomera region.
<BR>
<BR>

Follow the course of this <A href="http://www.starchaser.co.uk" target=_top>Starchaser</a> sponsored journey, which began with a special Mars Forum at Adelaide University on the evening of 26 November 2001. Then review the 
<a href="/jnt/Jnt1_itinerary.php">expedition itinerary,</a> read the <A HREF="#journals">expedition journals</A> of some of the participants (below), then see the stunning expedition <A HREF="#photos">photo gallery.</A> Read the <a href="/library/JarntimarraPressKit.pdf">Jarntimarra press kit</a> (pdf).
