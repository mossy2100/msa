<span class="bodytext">
<i>2002 marks the 30th Anniversary of Apollo 17 and Harrison's visit to the Moon, the last by humans...</i><BR><BR>


Harrison Hagan Schmitt, a native of Silver City, NM, has the diverse experience of a geologist, pilot, astronaut, administrator, businessman, writer, and U. S. Senator.  He received his B. S. in Science at Caltech, studied as a Fulbright Scholar at Oslo, and attended graduate school at Harvard.  He received his Ph.D. in geology in 1964 based on geological field studies in Norway.  As a civilian, Schmitt received Air Force jet pilot wings in 1965 and Navy helicopter wings in 1967.<BR><BR>

Selected for the Scientist-Astronaut program in 1965, Schmitt organized the lunar science training for the Apollo Astronauts, represented the crews during the development of hardware and procedures for lunar surface exploration, and oversaw the final preparation of the Apollo Lunar Module Descent Stage.  He was designated Mission Scientist in support of the Apollo 11 mission.  After training as back-up Lunar Module Pilot for Apollo 15, Schmitt served in that same capacity on Apollo 17 - the last Apollo mission to the moon.  On December 11, 1972, he landed in the Valley of Taurus-Littrow as the only scientist and the last of 12 men to step on the Moon.<BR><BR>

In 1975, after two years managing NASA's Energy Program Office, Schmitt fulfilled a long-standing personal commitment by entering politics in 1976.  He served in the U.S. Senate from 1977 through 1982, representing his home state of New Mexico.  Senator Schmitt was a member of the Senate Commerce, Banking, Appropriations, Intelligence, and Ethics Committees.  In his last two years in the Senate, Schmitt held the position of Chairman of the Subcommittee on Science, Technology, and Space and of the Appropriations Subcommittee on Labor, Health and Human Services, and Education.  He later served on the President's Foreign Intelligence Advisory Board, the President's Commission on Ethics Law Reform, as Co-Chairman of the International Observer Group for the 1992 Romanian elections, as Vice Chairman of the U.S. delegation to the 1992 World Administrative Radio Conference in Spain, and as chairman of the Technical Advisory Board for the U.S. Army Research Laboratory.<BR><BR>

Harrison Schmitt consults, speaks, and writes on policy issues of the future, space, the science of the Moon, and the American Southwest.  He presently is Chair Emeritus of The Annapolis Center (risk assessment evaluation) and holds an appointment as Adjunct Professor in the Department of Engineering, University of Wisconsin-Madison, teaching "Resources from Space."  Schmitt's current corporate board memberships include Orbital Sciences Corporation and PhDx Systems, Inc., and he is a Member of the Corporation of the Draper, Laboratory.  He is a founder and the Chairman of Interlune-Intermars Initiative, Inc., advancing the private sector's acquisition of lunar resources and He-three fusion power and broad clinical use of medical isotopes produced by fusion-related processes.<BR><BR>

Schmitt's honors include 1973 Arthur S. Fleming Award, 1973 Distinguished Graduate of Caltech, 1973 Caltech Sherman Fairchild Scholar, NASA Distinguished Service Award, Fellow of the AIAA, Honorary Member of the Norwegian Geographical Society and Geological Association of Canada, 1989 Lovelace Award (space biomedicine), 1989 G.K. Gilbert Award (planetology), and Honorary Fellow of the Geological Society of America, American Institute of Mining, and Geological Society of London.  Dr. Schmitt has received several honorary degrees from U.S. and Canadian Universities.<BR><BR>

