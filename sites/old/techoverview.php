      <span class="bodytext"> <b>"Human factors�</b> has been an important consideration in US, Russian and other space programmes to date.  
Particular issues have included the impact of microgravity on human biological systems, design of living 
quarters, and group interaction in confined spaces.  These issues are even more important when considering 
the long duration space flight, communication lags and hostile environment to be faced by explorers on Mars.  <P>

A number of areas of human factors research are currently under consideration by MSA.  These include 
testing habitat design, crew interaction and crew selection on the proposed <A href="/marsoz.php">Mars-OZ</a> site at Arkaroola.  
Also under consideration are techniques for measuring human performance variables such as attention, 
fatigue and �situation awareness�.   Some proposed research focuses on human-machine interaction.<P>

