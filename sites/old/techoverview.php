Mars is within reach.&nbsp; The fourth planet from the sun, the Red Planet, is the
next great measure of our mettle.&nbsp; Mars is a potential new world for
humanity.  It has all the stuff needed to sustain a substantial human
presence. It has water, an atmosphere, mineral wealth. Simply
exploring the surface of another world will teach us a great deal about our
present one.&nbsp; And Mars is no longer just a distant dream.&nbsp; In the last
decade, major strides toward feasible, affordable human missions were made.
Sending humans to Mars is now the formal goal of NASA and the European Space Agency.  
The question is no longer, �Can we go?� but, �When will we go?�
<br>
<br>

The journey begins here, on the oldest continent, where we undertake comparative research
and test surface exploration strategies and technologies in renowned Mars
analogue locations. Our vision is to position Australia for a role in
establishing a human presence on Mars.
<br>
<br> 

One of the ways Mars Society Australia pursues this vision is by undertaking its own research projects, 
usefully contributing to the large body of knowledge that a human Mars mission will need to draw upon.  Summaries of 
some of our major projects are given below.  You can also view a full <A href="/bibliography.php">bibliography<A>
of MSA publications.
