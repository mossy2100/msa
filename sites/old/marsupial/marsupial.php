<b>MARSUPIAL</b><br><br>

The current final rover design is for a vehicle that can functionally accommodate a standard crew of 2 
for non-science exploration only sorties, with space for an 
optional third crew member for scienifically focussed field trips. It will also be able to operate in an emergency mode 
which entails accommodating four crew members for overnight periods. <br><br>
 
The rear section containing living and work spaces will be detachable and capable of 
operating for short periods as a portable emergency shelter. In this mode the rear tray of the vehicle may 
then be adapted to carry payloads or other equipment such as a small crane. 

<br><br>

<i>Project Stages</i><br>
			        <br>
      
				Construction of the rover has been separated into 3 stages. Stage 1 was funded by seed funding from the <a href="http://www.marssociety.org/">US Mars Society</a> awarded in 2001. Stages 2 and 3 are being sponsored by <a href="http://www.starchaser.co.uk/">Starchaser Industries</a>. <br>
			        <br>
      
				Stage 1 involved the purchase of a suitable vehicle chassis, and its refit and modification for use as the Marsupial rover. <br>
			        <br>
      
				Stage 2 is underway concurrently with Stage 3. It involves the design and construction of a plastic body to make the machine into an eye-catching, yet functional Mars rover. Most importantly it must effectively serve our needs as a test vehicle for trying out exploration strategies, equipment tests and plausible surface operation scenarios. <br>
			        <br>
      
				Stage 3 will complete an interior fit out of the vehicle. This will involve the purchase and installation of interior lighting, communications station, storage cabinets, bunks, spacesuit lockers, sink and toilet facilities. In effect, the vehicle will turn into a self- contained camper, ready for travel across the simulated Martian landscape.<br>
