                 <p><span class="bodytext">The Australian analogue rover project, the Starchaser Marsupial Rover was formulated with a longer term vision to develop a family of such rovers and provide Australians which could contribute to the design database for future real Mars rover vehicles. <br>
		            <br>
        
				Now based in Freemantle, Western Australia, the vehicle is based on the chassis of a Forward Control <a href="http://www.landrover.com.au/au/en/Vehicles/overview.htm">Landrover</a>. The FC Landrover has a distinguished history as a rugged 4WD military truck, emergency ambulance, expedition and safari transport. The purchased vehicle is a Series IIA, similar to the one in the photo. It is licensed, but will require some repair work before being ready to drive. We intend the Marsupial rover to be driveable on public roads, since this will greatly enhance its useability. </span></p>                 <p><span class="bodytext">Having published the results of his pioneering comparative studies of the North American rovers on <a href="http://chapters.marssociety.org/canada/expedition-mars.org/ExpeditionOne/">Expedition 1</a> in prestigous journals including the <a href="http://www.bis-spaceflight.com/sitesia.aspx/page/358/id/531/l/en-GB">Journal of the British Interplanetary Society</a>, the Starchaser Marsupial Project Manager Dr Graham Mann, is keen to undertake a similar series to trials with the Australian rover (to be known as <a href="/marsupial/trax.php">Marsupial Trax</a>)  once its construction is complete.<br>
