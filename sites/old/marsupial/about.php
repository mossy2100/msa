                 <p><span class="bodytext">Mars has a surface area roughly equivalent to that of all the earth's continents combined. Humans will be sent to Mars  to explore, to undertake scientific research and to locate resources. This will require the ability to safely travel long distances across the surface, for which they will need rugged, self-contained, all terrain vehicles. The requirements of Martian rovers will differ from those used in the lunar landings in a number of ways. They will need to be capable of undertaking long overland journeys, contain pressurised living quarters and be robust enough to operate for periods of years.<br>
		            <br>
      
				What is an optimal crew size for such a vehicle? How much internal space within the vehicle needs to be pressurised? How small can a vehicle be to undertake long distance traverses of +200km? What are the optimal exploration strategies? To date, little research has been undertaken into such issues.<br>
			        <br>
			        <b>Mars Society Rover Initiative<a name="Mars_Society_Rover_Initiative"></a></b><br>
			        <br>
      
				In 2000 the international Mars Society Rover Initiative (MSRI) provided three teams with seed funding to commence design and construction of working Mars pressurised rover simulation platforms, which could be used to investigate these kinds of questions. Two teams were from North America, the other was from the Mars Society Australia (MSA).<br>
			        <br>
      
				The vehicles were to be constructed to undertake human factors research. For this purpose, they were not required to be entirely custom made structures with the exact engineering tolerances and sophisticated internal life support systems a real Mars rover would require. (They were to be built on the chassis of commercially produced road vehicles and  run on standard petrol engines.) Instead, they were to realistically simulate the spatial and operational characteristics of such vehicles, with air locks, work, living and storage spaces. <br><br>
				Both the Michigan <a href="http://marsrover.engin.umich.edu/">Everest</a> and Canadian <a href="http://appsci.queensu.ca/news/2002-2003/ares/">Ares</a> rovers were built and trialed in carefully planned, simulated cross-country expeditions, which tested the effectiveness of each design configuration in different mission scenarios. Testing notably includes a series of comparative field missions undertaken in 2003 on the Australian-Canadian <a href="http://chapters.marssociety.org/canada/expedition-mars.org/ExpeditionOne/">Expedition 1</a> at the <a href="http://www.marssociety.org/MDRS/">Mars Desert Research Station</a> in Utah. <br>
