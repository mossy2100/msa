                 <p><span class="bodytext">The Starchaser Marsupial Rover is intended to be an experimental tool which with to generate comparative data on the advantages and disadvantages of different rover configurations and expedition scenarios through field testing. Therefore building the vehicle is only part of the story - a program of in depth field testing is integral to the vehicle serving its purpose.<br>
			        <br>
			        The trials and expeditions are to be collectively called “Marsupial Trax”, and it is proposed to run them in two phases, a trial phase and a research phase. 
  They will draw on the <a href="/">Mars Society Australia</a>'s previous experience at conducting Mars analogue field research in <a href="http://chapters.marssociety.org/canada/expedition-mars.org/ExpeditionOne/">Expedition 1</a> and <a href="http://chapters.marssociety.org/canada/expedition-mars.org/ExpeditionTwo/">Expedition 2</a>, 
  and the catalogue of Mars analogue sites it commenced developing with the Jarnitmarra Expedition in 2001.
  Project manager Dr Graham Mann already has tested analogue human Mars rovers, having 
  undertaken a pioneering comparative field test of 2 other Mars analogue rovers (the Canadian <a href="http://appsci.queensu.ca/news/2002-2003/ares/">Ares</a> and 
  the <a href="http://marsrover.engin.umich.edu/">Everest</a> vehicle from Michigan) at Utah in 2003.</span><span class="bodytext"><br>
                    <br>
    
                    <b>Expedition Phases</b><br>
                    <br>
                    The trial phase will be held in Western Australia. It will confirm the operational status of the rover and develop the user manual. It will most likely consist of a series of short expeditions, which will be named Trax 1.1, Trax 1.2 etc. <br>
                    <br>
                    The research phase will use Marsupial as an operational platform. Research activities will include real world field science and engineering under simulated EVA conditions and human factors research into real world rover traverses.  
Expedition planning, funding, and publicity will be undertaken as national MSA initiatives.
  
<br><br>
  
There could potentially be a large number of expeditions during the research phase, reflecting the diverse range of possible mission scenarios. These could be conducted in full or partial simulation mode. Possible topics of investigation include the performance of the rover design with different crew sizes (it was designed for 2), of its performance with a simulated mission long full vehice depressuration, with an injured crew member, with undertaking different type of science missions, and with missions of varying travel distance and duration. The intention is to publish the findings of the research phase in peer reviewed journals. </span></p>
