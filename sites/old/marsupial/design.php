The rover design has evolved considerably since the project commenced.

 The original competition entry submission featured a large, custom engineered, dynamically shaped vehicle  with large retracting wheels known as 'Wombat'. This was superceded by a more easily engineered design 'HOP' (Human Operations Prototype), which could be built with a more accessible budget. Since the project moved to Perth in 2002, the concept was updated to become the design layout known as 'Marsupial'. An add-on robotic arm feasibility study also has been undertaken, tentatively known as the 'M-Paw'. <br>
