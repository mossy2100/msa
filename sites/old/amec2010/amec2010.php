<b><span style='font-size:18.0pt;font-family:Arial'><a name="public_lecture">FREE OPENING PUBLIC LECTURE</a></span></b></p>
<p>

			<a href="/images/Adrian_Brown.jpg">
<img src="/images/Adrian_Brown.jpg" align="left" width="130px" height="146px" style="border:4px solid white;" ></a>

			<a href="/images/Jennifer_Heldmann.jpgG">
<img src="/images/Jennifer_Heldmann.jpg" align="left" width="130px" height="146px" style="border:4px solid white;" ></a>



The conference will open on the evening of Friday 9th July with a free public lecture by Dr Adrian Brown and Dr Jennifer Heldmann of NASA Ames, beginning at 6.30pm.   Their presentations will include updates on the LCROSS lunar mission and on data collected by the CRISM instrument aboard the Mars Reconnaissance Orbiter, as well as advice for applicants for the VSSEC-NASA Australian Space Prize.  Download a lecture tour <a href="/library/Adrian-&-Jennifer-Space-Prize.pdf">flyer</a>.<br><br>

Dr. Adrian Brown is a research scientist at NASA Ames Research Center and the SETI Institute in Mountain View, CA. He is a graduate from the Australian Centre for Astrobiology at Macquarie University, where he worked on mapping hydrothermal systems in the 3.5 billion year old rocks of the Pilbara in Western Australia. Adrian is currently working on the Compact Reconnaissance Imaging Spectrometer (CRISM) on Mars Reconnaissance Orbiter. Dr Jennifer Heldmann is a research scientist at the NASA Ames Research Center. She obtained her PhD from University of Colorado, where she studied the nature of gully features on Mars. She is currently working on the Lunar CROSS Mission, which impacted the Moon on the 9th October 2009 in search of water in the Moon's poles. Jennifer is a graduate and former staff member of the NASA Ames Academy.

