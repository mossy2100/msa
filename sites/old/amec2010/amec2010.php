<p>The 10th Australian Mars Exploration Conference (AMEC 2010), will be held in Melbourne in on the weekend of 9th-11th July of this year. Anyone from the research community through to the interested member of the public is welcome to attend.  The weekend will commence with a free public lecture on the evening of Friday the 9th July (details to be announced shortly), with a full programme of presentations throughout the weekend. A fabulous conference banquet will be held at Melbourne's landmark <a href="http://www.youngandjacksons.com.au/">Young and Jackson's Hotel</a> on Saturday evening (booking essential).  Conference registration will include lunch on both days.  What a bargain!  Visit our <a href="http://www.facebook.com/event.php?eid=124014837628239">Facebook Event page</a>. 

