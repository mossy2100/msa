Two of our Board of Directors, Mark Gargano and Dr Graham Mann presented the key note session on Thursday 20th January to the 2011 WA Science & Engineering Summer School, an event hosted by the Science Faculties at Murdoch University in Western Australia. <br><br>

The first half of the presentation, conducted by Mark, outlined a history of space science, including Australia&#39;s involvement with rocket testing, communication and astronomy. This then expanded to areas in WA where the study of space is currently being pursued, including the various observatories and the European Space Agency Tracking Station at New Norcia as well as radio-astronomy and the Australian Square Kilometre Array Pathfinder (ASKAP). From here discussions about projects linked with MSA, including NASA Spaceward Bound, and the range of sciences being studied were outlined, including how local scientists and teachers have been involved previously with expeditions in the Mojave Desert, South Australia, Namibia and Utah and are all very much looking forward to expanding the boundaries of knowledge with Spaceward Bound Australia in July this year, by examining early life on Earth through research on stromatolites. <br><br>

Highlights of this part of the talk included the on-the-spot-space science quiz and demonstrating the use of the MarsSkin 3 space suit suit designed by our very own Dr James Waldie, with connections made to conducting science in the field while having mobility to collect samples and perform key geology tasks. Dr Mann expanded this further with his personal experiences from the Mars Desert Research Station (MDRS) as well as showing some of the capabilities and necessities of robots in space research, demonstrating the hexapod robot, Mascot that he is designing and constructing. <br><br>

Mark Gargano comments that &#34;It is important for students to see the local connection to space science research and development, which promotes areas of study and careers that they may have not previously considered. It is essential for secondary students to be able to hear and see the latest in exciting space science projects, demonstrating that they too can participate in authentic activities and contribute genuinely to research, creating the next generation of scientists, engineers and explorers. &#34; <br><br>

Mr Gargano adds, &#34;It is through the assistance of holiday experiences and sessions such as the WA Science and Engineering Summer School that re-enforces the work being done in the classroom and widens the experiences and the outlook of attending students. When they return to school, topics covered within areas of science, will re-visit with depth some of points raised and ideally jolt the students into considering the diverse options available for further study, so that you can contribute globally by researching locally&#34; <br><br>

This year&#39;s group of 59 Year 10 and 11 students came from all over the state, for the week of activities, lectures, laboratory sessions, field trips and career presentations. The WA Science and Engineering Summer School is coordinated at Murdoch University by Ms Yolanda Pereira, for further details on this senior school programme she can be contacted on; Y.Pereira@murdoch.edu.au. <br><br>
This was the second student presentation conducted that week by Mark, on Tuesday he spent the day with 28 students from the National Youth Science Forum (NYSF) at the Gravity Discovery Centre (GDC), just north of Perth, expanding their minds to the wonders of Physics with an Einsteinian approach. <br><br>

With the team out there at the GDC, investigations were conducted with vortex rings, interference patterns, measuring the force of gravity and discussing the types of units that motion in spacetime should be measured with. <br><br>
With activities such as these, the future of space science must be looking up! <br><br>



