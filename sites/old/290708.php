<span class="bodytext">

The <a href="http://www.hobbycentre.com.au/SBA2008Reg.html">Spaceward Bound Australia 2008</a> expedition has been successfully completed. 
Thirteen people from the US, Europe, and Australia took part in this preliminary 
trip examining sites and facilities for next year’s full Spaceward bound expedition 
for Australian and US teachers and researchers. Notable participatnts included 
Dr Liza Coe, Deputy Chief of NASA Ames Education Division, and Artemis Westenburg, 
Director of International Relations of the International Mars Society Steering Committee. Sites visited included the historic launch sites at Woomera, the Mars analogue terrain at Arkaroola, artesian springs at gullies at Reedy Springs, the Beverley uranium mine, and various sites in the Flinders Ranges.
<br>
<br>

<b><i>Woomera Report</i></b>
<br>
<br>

As a prelude to the field science in Spaceward Bound Australia, the
group of 13 expedition members examined areas in and around Woomera.
This vital phase highlighted areas that may be expanded on for
Spaceward Bound-2 in 2009. The activities demonstrated parts that
are useful and interesting to attending educators that will expand
the field component of the expedition. As an educator, it is always
important to have an understanding and create the concept within
your students of the broad scientific and technological world.
Looking through the different sites around Woomera will permit
teachers to link our Science to the historical perspective, within a
technological framework to understand, from where we have come?
<br>
<br>

After taking in the Heritage Centre, which starts with a short post-
World War Two documentary describing the problems faced by the UK
and the allies from the first inter-continental cruise missiles,
developed by Nazi scientists and fired by the Germans to cause
maximum havoc and destruction.
<br>
<br>

Attendees then walked through the various exhibits that showed the
developing technologies that were trialled at Woomera post 1947 and
the equipment that monitored this hardware. For teachers of space
science, looking at artefacts of the Redstone that launched WRESAT,
images and memorabilia of the Black Arrow, various sounding rockets,
Skylark launches and the involvement of the European Launch
Development Organisation, permitting an appreciation of the
background to this town's existence.
<br>
<br>

An additional demonstrated feature of international collaboration
was viewed at Nurrangar, the former US and Australian Joint Defence
Facility, where members examined the giant golf ball (or Buckyball
if so inclined) like structure that houses the only remaining dish
on site. Getting up close and personal gives you a sense of scale
and of the cold war itself, as you climb over and through the
structure and walking through the buildings of the former secret
satellite communications and tracking facility.
<br>
<br>

From here the tour party turned our attention to the pair of large
launch pads sitting over Lake Hart where many of the major events in
the 1960 to early 70s occurred. The next phase highlighted an area
of modern technology and the collaborations occurring between
Australia and Japan with high energy observations at the Gamma Ray
Observatory.
<br>
<br>

The following day began with a guided tour of the Bureau of
Meteorology outpost at Woomera, where various aspects of recording
and monitoring weather using diverse methods including balloon
launches were demonstrated. The expedition time finished with a tour
of Missile Park and the museum that highlight a range of important
equipment that was tested at Woomera, including the Jindavik, Blue
Streak missile, Black Arrow missile and the remains of the Redstone
that took Australia into space in 1967.
<br>
<br>

A social event was held on our last night in Woomera at the Bowling
Centre, an easy way to unwind and show our sporting talents before
we hit the field and take in the geological features of the Flinders
Ranges. 
