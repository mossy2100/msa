<br>

<span class="bodytext">


In November 2010, the Asia Pacific Regional Space Agency Forum (APRSAF) will be held in Melbourne. This event will be co-hosted by the Department of Innovation, Industry, Science and Research (DIISR) and the Japan Aerospace Exploration Agency (JAXA) and promotes collaboration between the regions space agencies. To support this objective, and promote awareness of the benefits of space technology, the APRSAF Space Education and Awareness Working Group has established a poster design competition for students aged 8 to 11 years, a Water Rocket Event for students aged 12 to 16 years, and an Educators Workshop to be held in association with the Water Rocket Event. <br><br>
The event has previously been held in Japan, Indonesia, India, Vietnam and Thailand. The event promotes regional collaboration and the use of space to engage students in the study of science. The addition of an Educators Workshop provides an opportunity for teachers to exchange ideas and build skills in areas of space science. <br><br>

</span>

<span class="headlines">

National Competition to Select Australian Participants</span>
<br><br>

<span class="bodytext">

There will be a National Competition will to select a student and teacher from each Australian state and territory to attend the APRSAF Water Rocket Event and Educators Workshop. Thanks to the Space Policy Unit and the Engineers Australia National Committee for Space Engineering all travel, accommodation and meals will be provided. Additional places are available for Australian teachers wishing to attend the Educators Workshop. <br><br>
Applications for the Water Rocket event close Friday 22nd October 2010. <br><br>
The APRSAF Poster design competition for 8-11 year olds closes on Monday 11th October 2010.<br><br>

