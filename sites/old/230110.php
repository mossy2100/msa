WA Science & Engineering Summer School
</span>
<span class="bodytext">
<br>
<br>
<i>23 January, 2010</i>
<br>
<br>


</span>
<br>

<span class="bodytext">

Mark Gargano, MSA Education Director, presented his keynote presentation at the 2010 Western Australian Science & Engineering Summer School on the 20th of January. The Summer School was hosted by Murdoch University and aimed to give the 59 attending Year 11 and 12 students a chance to see and experience science, mathematics and engineering in action; to learn from and to work with some of Australia's leading researchers; to explore challenging career paths and experience what life as a tertiary student might be like. <br><br>
Each day the programme begins with a one to one and a half hour keynote address followed by practical sessions and tours of laboratory facilities and scientific agencies and research groups on and off campus. This year, the Wednesday morning keynote was devoted to space science. Mr Gargano discussed areas where students can get involved in Earth, planetary and space science at a local level, an overview of Mars studies and then highlighted the areas of research and development that are currently occurring, incorporating discussions on stromatolites here in WA at Shark Bay, Lake Thetis and the Pilbara, making a link to the big space picture. <br><br>
In addition to general information, Mark outlined Mars Society Australia and the various programs that are under development, including the Starchaser Marsupial, Mars-Oz, field work and Project MarsSkin. &quot;The students showed a lot of interest in the topics presented and this was demonstrated by the high level of questions received at the end of the presentation. The interest factor increased remarkably in the middle of the talk when one of the Summer School staff entered at the pre-arranged time wearing the MSA spacesuit coveralls, with helmet and backpack. The students understood visually very quickly what a projected suit for Mars would look like and why this style will make long-term scientific research on the red planet far more viable than the Apollo era suits&quot;, explains Mark Gargano.<br><br>
In addition, a show case of previous expedition work through NASA Spaceward Bound was mentioned, with links made to students about the relationship of analogue testing in various remote locations such as the Mojave desert and the Flinders Ranges in South Australia and what this means for space research. &quot;It is apparent that space research is of much interest to our youth and particularly how they can contribute and be involved in this exciting area in the future, perhaps even as field scientists on a future expedition&quot; adds Mark. <br><br>

The aim of the experience was to highlight careers and motivate students to continue with their studies progressing towards Science & Engineering, with the enthusiasm displayed, hopefully we will see many fresh faces in the next few years in space science in Australia. Thanks are noted to the WA Science & Engineering Summer School team, specifically Ms Yolanda Pereira for this outreach opportunity. <br><br>

