<FONT COLOR="#FF0000" FACE="ARIAL" SIZE="5"><B><I>Announcing the 8th Annual Mars Exploration Conference<br> (AMEC 2008).</I></B></FONT><P>

 

Presentations over this two-day event will cover a range of issues concerned with forging, nurturing and developing the pioneering culture and methods required to explore Mars.<P> Written submissions from scientists, engineers, social theorists, managers, writers and artists are welcomed. Full papers will be reviewed and published in accordance with the DEST guidelines.<P>
Topics include (but are not limited to): strategies and technologies for surface exploration; field science for exploration; recruiting the next generation of explorers, astronauts, scientists and engineers; educational needs for a spacefaring culture; the Mars Society as a culture; new concepts in project management; remote-area engineering; Mars as an inspiration to artists.<P>


