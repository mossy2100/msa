<b>Title:</b><br>
"Mars Exploration Rovers: Overview of Geologic, Geochemical, and Mineralogic Results"<br><br>


<b>Abstract:</b><br>
This talk will review the images, spectra, and in situ chemical/mineralogic information that the Mars Exploration Rovers Spirit and Opportunity have been acquiring from two sites on the martian surface for more than five and a half years.  The data provide quantitative evidence that liquid water has played a role in the modification of the martian surface during the earliest part of the planet's history.  At the Spirit site in Gusev Crater, the role of water appears to have been generally minor, although the recent discovery of enigmatic buried sulfate salt and amorphous silica deposits challenges that hypothesis.  At the Opportunity site in Meridiani Planum, geologic and mineralogic evidence suggests that liquid water was stable at the surface and shallow subsurface for significant periods of martian geologic time.  One exciting implication is that the regional environment at Meridiani, and possibly very local environments in Gusev, may have been "habitable" by some terrestrial standards early in martian history.<br><br>


<b>Bio:</b><br>
<a href="http://astro.cornell.edu/people/facstaff-detail.php?pers_id=93">Dr Jim Bell</a> is the Payload Element Lead for the Mars Exploration Rover "Pancam" multispectral, stereoscopic imaging systems on the NASA Spirit and Opportunity rovers.  He was a member of the science teams for NASA's NEAR, Mars Pathfinder, and CONTOUR missions, and is currently a member of the Mars Odyssey, MRO, MSL, and LRO mission teams.  He is a geologist by training and researches the geomorphology, composition, and mineralogy of planetary surfaces in order to understand their origin and geologic/geochemical evolution.  As the President of The Planetary Society, Jim is also an ardent public communicator of planetary mission results and is the author of many popular-level magazine articles and three recent space-related books: "Postcards from Mars," "Mars 3-D," and "Moon 3-D."<br><br>



