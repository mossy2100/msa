Australian Student wins Hakluyt Prize
</span>
<br>
<br>

<span class="bodytext">
A Melbourne year 11 student <A href="/mailto:felixdance21@yahoo.com">Felix Dance</A> won this year’s Hakluyt Prize, an international essay and letter writing competition run by the Mars Society.  <BR><P> 
Named after Richard Hakluyt, whose pamphleteering convinced the Tudor elite to launch the policies that led to the English colonization of North America, the Hakluyt Prize is awarded to students who write letters encouraging Mars exploration and distribute them to a broad base of influential individuals. This year is the third time the prize has been awarded.  This year's winners were: First Prize - Felix Dance, Australian Mars Society member; Second Prize to Bob Kopp, Washington, DC Mars Society member; and Third Prize to Jeff Marlow of the Rocky Mountain Mars Society.   Honourable mention awards were also given to Pittsburgh Mars Society member Sameer Soi, New England Mars Society member Richard Reifsnyder, and Lucas Shannon. <BR><P>
In his entry Felix focused on the need for an Australian space program.  He explained in his cover letter "I decided to be politically active in my own country ... by convincing the nation's politicians that a space program would benefit Australia ... in the mid and long term."  Felix sent his letter to every member of the senate and house of representatives, an impressive undertaking, and received some extremely encouraging replies, including one from Australia's shadow Prime Minister and Science Minister.  As Dance says in his letter: <BR><P>
"Australia is a nation founded by pioneers wishing to explore new lands ... As the world explores space, will we be left behind, to never be a part of humanity's most noble deed? No, Australia should be setting an example to the rest of the world ...This is an opportunity that should not be overlooked - for space is there and we are going to climb it."
Felix received a telescope and an all-expenses paid trip to the third Mars Society Convention in Toronto. 
