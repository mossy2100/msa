<A NAME="SAFMARS">
<span class="subheadlines">SAFMARS</span>  
<br>
<br>

<span class="bodytext">
<img  align=right width=150 height=130 src="/technical/tech_images/Safmars_on_black_25pc.jpg" width=150 height=130><valign=top>

SAFMARS (Store and Forward Mars Analogue Messaging System) is a theoretical investigation examining the possible use of 
the proposed <a href="http://www.asri.org.au/ASRI/research/satellite/JAESAT/index.xml">JAESAT satellite</a>
  and other Low Earth Orbit (LEO) satellites operating in the amateur radio frequencies (AMSATS) to establish a low-cost email-based 
communications system. Using portable ground stations, researchers at Mars-analogue 
sites in Australia (e.g Mars-OZ) and potentially on other continents could to communicate a range of data types 
(emails, images, monitoring data).<br>
<br>
 
See the original <a href="/library/MSA-TEC-SAF-OCD-1A.pdf">
SAFMARS Operational Concept Definition (pdf)</a> (1.08 Mb). 
