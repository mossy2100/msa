<br>
<br>
A draft conference program may be downloaded <a href="/amec2008/AMEC2008_Program.pdf">here</a>.  Confirmed speakers include the following...<br><br>

<b>OPENING PUBLIC LECTURE - 'Australian Hypersonic Research and the Road to Mars'. </b><br>
<b>Professor Russell Boyce</b>, <i>DSTO Chair of Hypersonics, Division of Mechanical Engineering, University of Queensland</i><br>
Professor Russell Boyce gained his PhD in Hypersonics from the Australian National University in 1995, and prior to his current position held postdoctoral positions at the University of Queensland Centre for Hypersonics and Imperial College of Science, Technology & Medicine, London, and a Senior Lectureship in the School of Aerospace, Civil & Mechanical Engineering at the University of New South Wales at the Australian Defence Force Academy in Canberra. He now holds the DSTO Chair for Hypersonics at the UQ Centre for Hypersonics, where he conducts fundamental research in hypersonic airbreathing propulsion and aerothermodynamics, and coordinates the Australian hypersonics network. He actively collaborates with international agencies and institutions such as the European Space Agency, the USAF, the German Aerospace Center, and the Japanese Aerospace Exploration Agency. Among other activities, he represents Australian hypersonics on the steering committee for the first Decadal Plan for Australian Space Science 2008-2017.<br><br>

Russell will be delivering this special public lecture on Friday, 4th July beginning at 7.30pm, 
Entry is by gold coin donation, attendance is open to all.<br><br>

<b>SPECIAL CONFERENCE GUEST</b><br>
<b>Professor Rob Morrison OAM, South Australia 's Senior Australian of the Year 2008.</b><br>
Professor Rob Morrison's work as a scientist, writer and media personality has added profoundly to the promotion and understanding of science in Australia. For 25 years he has been a familiar face of science on television, as presenter of The Curiosity Show for eighteen years and appearances on The New Inventors, Nexus, Science Magazine, and Radio National's Ockham's Razor. He has written thirty books on science and natural history and under his stewardship the SA Royal Zoological Society has trebled its membership in six years. Rob recently helped establish SciWorld, a new science centre in Adelaide which runs exciting education programs and developed six regional science fairs and shows, including Adelaide's National Science Week. He is recognised nationally and internationally as an outstanding science communicator and in the citation for his 2007 Australian Museum Eureka Prize Rob is described as a national treasure and a well-loved ambassador for science.  Rob's talk will be titled  'Trust me, I'm a Science Communicator!'<br><br>

<b>OTHER CONFIRMED SPEAKERS</b><br>
<b>Dr Mark Bishop</b>, <i>Senior Lecturer, School of Natural & Built Environments, University of South Australia and Associate Research Scientist
Planetary Science Institute, Tucson, Arizona</i><br>
Mark has research degrees in physical geography, geomorphology, and spatial statistics and some 20 years of service as a geoscience educator in the tertiary sector. His research involves the application of geographical information systems (GIS) based spatial analysis to the landforms of Mars and Earth.  
The title of Mark's talk is 'Exogeography: A Paradigm for the Further Understanding of Climate Change on Mars and Earth'.<br><br>

<b>Dr Jonathan Clarke</b>, <i>Vice-President, Mars Society Australia</i><br>
Jon is a geologist with 25 years experience in industry, academia, and government. Jon will be talking about missions and roles for rovers supported crewed missions to Mars.<br><br>

<b>Dr Lizabeth K Coe</b>, <i>Assistant Chief (Acting), NASA Education Division</i><br>
Spaceward Bound: Training the Next Generation of Explorers
<br><br>

<b>Mr David Cooper</b>, <i>President, Mars Society Australia</i><br>
Based in Western Australia, David is a former commerical pilot who now runs hobby shop outside Perth. His talk will be on  'Mars Society Australia, 2017AD'.<br><br>

<b>Mr Michael L. Darby</b>, <i>Lecturer/Researcher, Curtin University of Technology</i><br>
Michael is a Lecturer in the Department of Electrical and Computer Engineering at Curtin University of
Technology, Perth WA. He has over 30 years experience in Modelling
and Simulation with the US Department of Defense (DoD), Australian
Defence Force (ADF), Industry, Standards Organisations, research
laboratories, and Academia experience. 
His presentation is titled 'Exploring Knowledge beyond the Rutted Path: Aligning Computer Based Technologies with New Curricula for Teaching and Learning High School Science and Mathematics'. <br><br>

<b>Mr Mark Gargano</b>, <i>Education Officer Mars Society Australia, Inc. </i><br>
Mark is Science Coordinator at St Joseph's School, Northam, in Western Australia and a participant in the upcoming Spaceward Bound Mojave 2008 Expedition. Mark will present 'Creating the Next Generation of Space Explorers Now'.<br><br>

<b>Mr Hugh Gregory</b>, <i>Spaceflight Historian, 
Spokesperson for the Orbital Commerce Project
 </i><br>
Hugh's presentation will be on the Orbital Commerce Project.<br><br>

<b>Mr Steven Hobbs</b>, <i>87 Squadron, RAAF Edinburgh, South Australia
 </i><br>
Steven's talk is titled 'Colouring Mars: Revisiting Historical Spacecraft Imagery'.<br><br>

<b>Ms Erita Jones</b>, <i>Director, Mars Society Australia
 </i><br>
Erita's talk will be 'Modeling the Martian Subsurface In Search of Water'.<br><br>

<b>Dr Graham Mann</b>, <i>Director, Mars Society Australia, Lecturer, School of Information Technology Murdoch University Murdoch Western Australia
 </i><br>
Graham's talk will be 'Quantitative Evaluation of Human-Robot Options for Maintenance Tasks during Analogue Surface Operations'<br><br>

<b>Mr Trevor Rodwell</b><br>
Trevor Rodwell holds a Bachelor of Design (Honours) from the University of South Australia and a Graduate Diploma in Business Enterprise from The University of Adelaide.  He is currently undertaking a Research Masters in Communication at the University of Canberra.  He works a an artist mainly in the fields of Public Art and Land Art. The title of Trevor's talk will be 'Messages for the Future - the Concept for a First Human Landing Marker on Mars'.
<br><br>

<b>Ms Jenna Sharpe</b>, <i>Monash University
 </i><br>
Jenna's presentation is on the site selection process for the Mars Science Laboratory Mission.<br><br>

<b>Ms Artemis Westenberg</b>, <i>President, Mars Society Netherlands, Director of International Relations for the Mars Society (US)</i><br>
Artemis has an M.A. in History, (Classical Studies, International Relations and Islam), from Leyden University. She has organised many Mars Society gatherings and conferences in the Netherlands, including the second and seventh European Mars Society conferences. Artemis is a member of the Mars Society International Steering Committee, and has served on 5 crews at the Mars Desert Research Station.<br><br>

<b>Mr David Willson</b>, <i>Director, Mars Society Australia and Mars-Oz Project Manager</i><br>
David is a mechanical engineer with background in major materials handling infrastructure projects for mines and ports. He also has co-authored a number of papers on manned Mars missions. His presentation will be, 'MARS-Oz: A Proposal for a Simulated Mars Station at Arkaroola in South Australia, providing an Inspirational Setting for Science Education and a Focal Point for Australian Planetary Science.'
