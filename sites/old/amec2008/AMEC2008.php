<font face="ARIAL" size="2"><i>
The 8th Australian Mars Exploration Conference will be held in Adelaide
over the weekend of <b>4th - 6th July, 2008</b>. Presentations over this
exciting two-day event will cover a range of issues concerned with
forging, nurturing and developing the pioneering culture and methods
required to explore Mars.  

The conference also serves as a prelude to the <a href='http://quest.nasa.gov/projects/spacewardbound/' target='_blank'>Spaceward Bound</a>-Australia expedition immediately following it.  MSA will be taking a group of educationalists on a learning and development trek to Woomera, Arkaroola and the central Flinders ranges.  They will examine sites of past and present space activity and those with Mars analogue significance and which illustrate major events in the history of Earth. A number of participants are expected to make presentations AMEC. <br><br>

Download a  
<a href="/amec2008/AMEC2008_Flyer.pdf">conference brochure</a> or a 
<a href="/library/media_release_010708.pdf">media release</a>
and help spread the word!<br><br>
