If you are a Secondary School Teacher with an interest in earth, planetary
and space science, then the Spaceward Bound Mojave 2011 expedition might be
just the professional learning for you.<br><br>

This exciting opportunity is to be held in the Californian Mojave Desert
from the 17th to the 21st of April. Spaceward Bound Mojave 2010 is a
professional development program aimed at helping educators find exciting
new ways to teach and link science. Participants can expect high quality
professional activities and fieldwork in desert soils and microbiology,
geomicrobiology, astrobiology, hypolithic algae, remote sensing and
engineering. It is envisaged that the successful Australian participant will
fly into and depart from San Francisco, linking in with other expedition
members at the NASA Ames Research Center. You will have an opportunity to
spend time working in the field with leading planetary scientists in
disciplines including geology and astrobiology.<br><br>

NASA Spaceward Bound have once again extended this fantastic opportunity to
Australian teachers and one lucky person in 2011 will gain an experience of
a lifetime working with scientists, engineers and educators in the Mojave
Desert.<br><br>

If this sounds like the thing for you, read more and download an application form <a href="/library/Spaceward_Bound_Mojave_2010_Application.doc">here</a> and complete your submission by 8th December, 2010.

