<h2 align="center" style="color: rgb(255, 0, 0);"><i>  
From Marree to Lyndhurst</b><br> 
</i></h2> 
<p>
<b><i>
14th July, 2009</b></i><br>
After spending the night in the historic Marree hotel, the expedition today moves from Marree to Lyndhurst, splitting up into groups to undertake a variety of field activities.  One party is chartering a helicopter to fly over Clayton Springs and take aerial photographs of the surrounding area.  Another is heading up the Birdville track looking for microbial crusts, while another party has travelled to Lyndhurst early to set up the laboratory.  Several of the groups begun by visiting a particularly interesting rock field near the Marree airfield pointed out by Chris McKay, which includes colonies of algaes beneath some of the quartz stones.  More photographs have now been uploaded to the <a href="/SBA2009_galleries.php" target=_self>photo galleries</a> from previous days.  Keep leaving your questions and comments on the expedition <a href="/SBA2009_messageboard.php" target=_self>messageboard</a>!
