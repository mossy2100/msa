<h2 align="center" style="color: rgb(255, 0, 0);"><i>  
Reedy Springs</b><br> 
</i></h2> 
<p>
<b><i>
15th July, 2009</b></i><br>
With some heavy showers last evening, there was some uncertainty as to whether the road to Reedy Spring would be open this morning, but at 9am we got the all clear to go and the team headed off.  Along the way we stopped at several sites of relatively barren gibber desert to look for desert crusts, finding only a light, very scattered distribution.  Reedy Spring is a large gully system fed by slowly seeping artesian springs.  Some substantial areas of microbial mats were identified and sampled.  (More detailed accounts of the scientific interest of the springs are included in the <a href="/SBA2009_reports.php" target=_self>reports</a> section).  Some of the teachers present also tried on and tested the analogue suits we have brought with us from the Victorian Space Science Education Centre.  In the evening Dr Penny Boston gave a talk on her research into microbial mats in caves and deserts. <br>
