<h2 align="center" style="color: rgb(255, 0, 0);"><i>  
First Day in the Field</b><br> 
</i></h2> 
<p>
<b><i>
11th July, 2009</b></i><br>
The first day at Arkaroola was spent undertaking reconaissance visits to a number of sites of scientific interest.  We begun by visiting the spectacular dried gorge along Stubbs Creek, then driving on to Paralana Hot Springs were we stopped to have lunch.  Paralana Hot Springs are a remarkable water source which is heated by a combination of geothermal heat and radioactive decay.  The waters emit radon gas and are mild radioactive.  As such, the site is of considerable interest to astrobiologists as an abode for extremophiles, and an analogue for similar features on Mars.  One the way back the team split into two groups, which commenced undertaking field sampling work on the way home.  Wildlife including emus and an echidna were sighted.  In the evening we had a special dinner outdoors, and afterwards listened in to a discussion between the various field scientists as to their strategies for the next few days.<br><br>

Arkaroola being a remote location mean that internet access is limited, however we intend to post news stories, field reports and photos over the coming days, so stay tuned.  Also, feel free to post comments or questions for the expedition team on the <a href="/SBA2009_messageboard.php" target=_self>Messageboard</a>.
