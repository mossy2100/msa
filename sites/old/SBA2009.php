<h2 align="center" style="color: rgb(255, 0, 0);"><i>  
Some Serious Science</b><br> 
</i></h2> 
<p>
<b><i>
12th July, 2009</b></i><br>
Today the serious science started, with the expedition party dividing off into a number of smaller groups which fanned out over the local region.  Science focussed on the investigation of microbial crusts, desert varnishes, lichen colonies, water hole/spring communities and fossilised microbial mats and stromatolites.  Read some of the <a href="/SBA2009_reports.php" target=_self>field reports</a>. Several of the team went on a flight over Arkaroola to take aerial photographs, courtesy of the resort owner, Doug Sprigg. In the evening there were lectures by Dr Jennifer Heldmann on a forthcoming lunar impactor mission, by Dr Adrian Brown on results from the Mars Reconaissance Orbitor and by David Willson on space design projects for students. The accompanying film crew undertook filming for a forthing space documentary on National Geographic during the day, including an extensive interview with Dr Chris McKay.<br><br>

