<br>

<span class="subheadlines">Psychological Studies on Expedition One, Utah, USA.
</span>
<br>
<br>

<span class="bodytext">
During Expedition One (ExOne) in February-March 2003 in Utah, USA, a range of psychological measures, 
informal observation, crew discussion and other means were used to collect information about crew 
psychological issues. The overall goal of the psychological studies was to gain insight into crew 
individual and group issues that may be relevant to a human mission to Mars or other prolonged human 
spaceflight. Another goal was to gather information relevant to improved functioning for future Mars 
simulation crews.  The psychological data was collected by several crewmembers including 
<script> email('amaurysolignac','hotmail.com','Amaury Solignac')</script>, psychologist from France, 
Dr Nishi Rawat, medical resident, at John Hopkins Hospital in Baltimore USA, MSA President 
<script> email('president','marssociety.org.au','Guy Murphy')</script>
 and myself.  Several research teams were represented from Australia, USA and Canada.  Some details are 
reported below while the post ExOne psychology report is available 
<A HREF="http://www.marssociety.org/MDRS/fs02/crew14/psycho.asp">here</A>.
<br>
<br>

<b>Social Psychological Measures</b>
<br>
All crew members completed a questionnaire entitled "Personal and Group Functioning Survey". This 
instrument was developed by social psychologists 
<script> email('katherine.reynolds','anu.edu.au','Dr Kate Reynolds')</script> and 
<script> email('rachael.eggins','anu.edu.au','Dr Rachael Eggins')</script>
 at the <A HREF="http://www.anu.edu.au/psychology/">School of Psychology, Australian National University</A>.
 It is based on an extensive literature and research on issues of group identity and goal alignment 
conducted by the Social Psychology Research Unit at ANU. It aims to help determine the extent to which 
crew members on ExOne identified with ExOne overall as well as with it's subgroups such as Field Science 
and Mission Systems. In turn it aims to measure the degree to which crewmembers aligned their personal goals 
with the overall ExOne group and it's subgroups. 
<br>
<br>

<b>Neurocognitive Measures</b>
<br>
All crewmembers also completed a brief, computerised measure of cognitive performance called 
<A HREF="http://cogstate.com/">CogState</A>. This measure was developed by a team of neuropsychology 
researchers in Melbourne including Assoc. Prof. Paul Maruff.  The team has developed highly sensitive 
measures of changes in neurocognitive status, currently being applied to medical and sports medicine 
settings but also potentially applicable to human space missions. Data from ExOne is being analysed 
as a separate neuropsychological study as well being incorporated into the social psychology study.
<br>
<br>

<b>Personality Measures</b>
<br>
To supplement measures of group and individual function crewmembers also completed a personality 
battery currently used by NASA to help select astronauts. 
<script> email('htsbishop','utmb.edu','Assoc. Prof. Sheryl Bishop')</script>
 of <A HREF="http://www.utmb.edu/iutmb.asp">University of Texas Medical Branch</A> provided the 
personality measures and is currently collaborating on data analysis and interpretation.  
<br>
<br>

<b>Results</b>
<br>
While results are not yet to hand, informal observation suggested that group identification was 
indeed a significant factor in both individual and group performance in this simulated Mars base 
environment.  Observation suggested that the extensive planning and group communication prior to 
ExOne resulted in an unusual level of shared group identity in crewmembers with resulting high 
levels of achievement of both group and individual goals.  Observation during neurocognitive measures 
suggested that group identification may have impacted cognitive performance but that other factors 
such as amount of sleep and workload were also significant.  Personality of individual crewmembers 
was observed to markedly impact both group and individual function.  In general crewmembers were 
observed to show a high level of cooperative behaviour in difficult conditions but on occasions 
friction related to personality differences was observed to disrupt both group cohesion and individual 
performance.  
