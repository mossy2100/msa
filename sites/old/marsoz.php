<A NAME="design">

<span class="headlines">
Base Design
</span> 
<br>
<br>

<span class="bodytext">
The facility consists of two 20 tonne road-transportable modules designed round concept mission using a “Mars Semi Direct” architecture and 
horizontally landed bent biconic spacecraft. The facility is designed to house up to 8 people at a time, and can undergo 
progressive expansion with additional modules, as required. A simulated Mars rover, the 
<A href="/" target=_top>Starchaser Marsupial</A>
 is being built in Perth, Western Australia and will form part of the base complex.<p>

The Mars-Oz concept employs a Hab, a Cargo vehicle and Mars Transfer Vehicle (MTV). 
(The Mars Transfer Vehicle is not discussed here.)  A summary of vehicle functions is given below.<p>

<A href="/images/base_complex.gif">

<img border="0" align=left width=517 height=210 src="/images/base_complex.gif" 
width=517 height=210></A><valign=top>

<br><br><br><br><br><br><br><br><br><br><br><br><br><br>

<ul> 
<li>  
<b>Habitat (Hab)</b> - Travels to the Martian surface, direct from earth and becomes the core of the Mars base. 
It consists of a cabin, propulsion module, heat shield, landing engines and parachutes.
Concept designs of the interior of the unit, include the floorplan and fittings have been prepared by 
<A href="http://kerstinthompson.com/" target=_top>Kerstin Thompson Architects</A>. 
(See the links below to floor plans, sections and perspective images).

<p>

<li>
<b>Cargo Vehicle</b> - Transports equipment to the Martian surface direct from earth 2 years prior to the arrival of the crew.
The equipment consists of a Mars Ascent Vehicle (MAV) for the crew to reach low mars orbit, hydrogen stock fuel, an in-situ resource 
utilisation processing plant, a pressurised rover and surface supplies for the crew. It also has a propulsion module, heat shield, 
landing engines and parachutes.<p>

<li>
<b>Mars Transfer Vehicle (MTV) </b> - Travels to low Mars orbit from earth. It transports the crew back to Earth from low mars orbit.
It consists of a cabin, landing capsule with heat shield, and propulsion module for earth return.<p>
</ul>

