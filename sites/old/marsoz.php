<A NAME="mission_plan">

<span class="headlines">
Mission Plan 
</span>
<br>
<br>

<span class="bodytext">
We have adopted a 62 tonne horizontally landed bent biconic spacecraft as the basis of the Mars-Oz vehicles. 
A detailed design concept has been completed of these vehicles for use in a real Mars mission and published 
in the Journal of the British Interplanetary Society May/June 2005 issue. 
<br>
<br>

<i>The Advantages of the Horizontally Landed Bent Biconic Spacecraft</i><br>

We find the horizontally landed bent biconic spacecraft is best suited for building Mars bases compared to other vehicle concepts.

They have superior cargo carrying capacity, easier loading and unloading, provides the biggest potential for growth by simply lengthening,
 offer good static and ground stability and simpler to provide radiation protection.  These vehicles must function for many years as part of a 
growing and changing Mars base. It is therefore desirable that the vehicle design is optimized as part of a Mars base rather than for traveling 
to Mars.
<br>
<br>

<i>Heavy Launcher Type</i><br>

The vehicles are of a size that can fit to the back of a shuttle or Energia launch system. The 62 tonne mass limit ensures a 100 tonne trans-mars 
booster can also be launched into Earth low orbit via the same launch system.

A LEO assembly is required in 2 parts. First the Hab or Cargo Vehicle is launched into LEO followed by a second launch providing the 
trans-Mars booster. The booster rendezvous with the Hab in LEO and then throws the assembly on to a trajectory to Mars.<p>

