<A NAME="intro">
<span class="headlines">
Introduction
</span> 
<br>
<br>

<span class="bodytext">
The centrepiece of MSA's technical program is the proposed Mars-Oz simulated Mars base.

The aim of the simulated base is to explore the issues of living and working on another planet. This includes facilitating research into social-psychological factors and related engineering design of crewed missions to Mars or the moon.  In summary the project goals are:

<ul> 
<li>  
To provide planetary scientists access to a ‘Mars like’ region enabling opportunities to conduct research and training for planetary geologists and astro-biologists in field exploration and their related methodologies;
<li>  
To provide geologists, biologists, psychologists, physiologists, engineers, designers and horticulturalists a ‘test bed’ to research ideas, methodologies and equipment that can operate within the constraints of a base on Mars;
<li>  
To empirically test of the design advantages of using bent biconic lifting body shapes as a design basis for Mars bases. In particular, internal utility and accessibility of working spaces, and the mobility of the modules when wheels are attached;
<li> 
To trial and demonstrate technologies suitable for environmental low impact self-sustaining mobile structures; and
<li> 
To provide an inspirational public outreach vehicle encouraging planetary exploration and the education of public groups and school students into the science and technology of living on another planet.
</ul>
These goals have driven the design of the Mars-Oz and the selection of a suitable location.
In addition, the proposed base located within the Arkaroola region can provide planetary scientists and engineers opportunities to evaluate different exploration methodologies, technologies and associated risks by undertaking field trials and simulations for both individual components and integrated systems. 

In particular field trials of unmanned rovers, manned rovers and space suit designs can be conducted over a wide range of terrain.
</span>
<br>
<br>

<A NAME="site">

<span class="headlines">
The Proposed Site
</span>
<br>
<br>

<span class="bodytext">
The preferred site for the Mars-Oz simulated base lies on the 
<A href="http://www.arkaroola.on.net/" target=_top>Arkaroola</A>
lease, Australia’s first and largest private nature preserve.  
The Sprigg family who run the property have a long history of supporting scientific research and eco-tourism. They are supportive 
of the establishment of a Mars analogue facility and the many visitors to the region provide an excellent opportunity for outreach.  
Arkaroola is eight hours by road north of Adelaide.  In an emergency the all weather airstrip at Balcanoona would permit air 
evacuation by the Royal Flying Doctor Service. The same airstrip can also be used by charter operators.<p>

An account of the site selection process which identified the Arkaroola region may be read
<A href="site_selection.shtml">here</a>, and detailed report describing the rationale for the 
specific site may be 
<A href="/library/marsoz_final_site_selection.pdf" target=_top>downloaded</A>
 as a pdf document.
</span>
