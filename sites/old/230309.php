One of the world's leading planetary scientists <a href="http://spacescience.arc.nasa.gov/people/mckay/">Dr Chris McKay</a> of NASA Ames will open this year's Australian Mars Exploration Conference (<a href="/AMEC.php">AMEC2009</a>) with a free public lecture of the evening of 17th July, 2009.  Titled 'Results of the Phoenix Mission to Mars and Analog sites on Earth', his lecture will report on the findings of NASA's recent <a href="http://phoenix.lpl.arizona.edu/">Phoenix</a> Mission. The talk will commence at 7.30pm at the Centenary Building, Level 3 - Room 16 (C3-16), University of South Australia, <a href="http://www.unisa.edu.au/about/campuses/ce.asp">City East Campus</a>, (Corner North Terrace and Frome Roads, Adelaide.
<br><br>

<b>Summary:</b><br>
<a href="http://phoenix.lpl.arizona.edu/">Phoenix</a> landed at 68 degrees N in the ice-rich ground on Mars and investigated the chemistry and geology of a polar site on Mars for the first time. The site is particularly interesting for astrobiology because 5 Myr ago the tilt of Mars' axis was 45 degrees and the amount of sunlight reaching the Phoenix site at summer solstice is 2x the present value - Earth like levels. Understanding the microbial activity in high elevation dry permafrost in Antarctica provides a basis for considering habitability conditions on Mars during these periods of higher obliquity.<br><br>

<b>Bio:</b><br>
<a href="http://spacescience.arc.nasa.gov/people/mckay/">Dr Christopher P. McKay</a> is a Planetary Scientist with the Space Science Division of NASA Ames.  Chris received his Ph.D. in AstroGeophysics from the University of Colorado in 1982 and has been a research scientist with the NASA Ames Research Center since that time. His current research focuses on the evolution of the solar system and the origin of life. He is also actively involved in planning for future Mars missions including human exploration. Chris been involved in research in Mars-like environments on Earth, traveling to the Antarctic dry valleys, Siberia, the Canadian Arctic, and the Atacama desert to study life in these Mars-like environments. His was a co-I on the Titan Huygen's probe in 2005, the Mars <a href="http://phoenix.lpl.arizona.edu/">Phoenix</a> lander mission in 2008, and the Mars Science Lander mission for 2011.  He is the deputy program scientist for Constellation - the NASA program for future human exploration of the Moon and Mars.<br><br>    


